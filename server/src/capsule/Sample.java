public class Sample {
  private int number = 0;
  private String str = "";

  public Sample(String str) {
    this.str = str;
  }

  public void setNumber(int number) {
    this.number = number;
  }

  public int getNumber() {
    return this.number;
  }

  public String getStr() {
    return this.str;
  }
}
